    // Certificate types by language
    public static String[] getCertificateTypes(String language) {
        return switch (language) {
            case "English" -> new String[]{"TOEIC", "IELTS", "TOEFL", "Cambridge", "Other"};
            case "日本語 (Japanese)" -> new String[]{"JLPT N1", "JLPT N2", "JLPT N3", "JLPT N4", "JLPT N5", "Other"};
            case "한국어 (Korean)" -> new String[]{"TOPIK I", "TOPIK II", "Other"};
            case "中文 (Chinese)" -> new String[]{"HSK 1", "HSK 2", "HSK 3", "HSK 4", "HSK 5", "HSK 6", "Other"};
            case "Français (French)" -> new String[]{"DELF", "DALF", "TCF", "Other"};
            case "Deutsch (German)" -> new String[]{"Goethe-Zertifikat", "TestDaF", "DSH", "Other"};
            default -> new String[]{"Certificate", "Other"};
        };
    }
